#Variável contador
x = 50

#Enquanto o valor da variável x for menor ou igual a 100, o laço continuará a fazendo tudo que tem dentro dele.
while x <= 100:
    #Será impresso o valor da variável x.
    print(x)
    #A variável x será incrementada, recebendo um novo valor.
    x = x + 1
