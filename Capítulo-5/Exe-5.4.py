#Variável contador.
x= 1
#O usuário irá digitar um número, que será armazenado na variável  num.
num = float(input("Digite um número: "))

#Enquanto x for menor ou igual a variável num, ele irá realizar tudo que estiver dentro do laço While.
while x <= num:
    #Será impresso na tela o valor da variável x.
    print(x)
    #A variável x será incrementada.
    x = x + 1
