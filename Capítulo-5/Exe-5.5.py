#Variável contador.
x = 1
#Variável
y = 10

#Enquanto a variável x for menor ou igual a y, ela realizará tudo que estiver dentro do laço While.
while x <= y:
    #A variável multiplo receberá a multiplicação do número 3 com a variável x.
    multiplo = 3 * x
    #Será impresso na tela o resultado da multiplicação 
    print(multiplo)
    #A variável x será incrementada.
    x = x + 1
