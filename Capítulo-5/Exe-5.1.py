#Variável contador
x = 1

#Enquanto o valor da variável x for menor ou igual a 100, o laço continuará realizando tudo que tem dentro dela. 
while x <= 100:
    #Irá ser impresso o valor da variável x.
    print(x)
    #x será incrementado, recebendo um novo valor.
    x = x + 1
