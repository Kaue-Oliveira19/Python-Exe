#O usuário irá digitar quantidade de metros, que será armazenado na variável Metros.
Metros = float(input("Digite a quantidade de metros: "))

#A conversão de metros para milímetros será armazenada na variável Milimetros.
Milimetros = Metros * 1000

#Será impresso na tela o resultado da conversão.
print(f"{Metros}m = {Milimetros}mm")

